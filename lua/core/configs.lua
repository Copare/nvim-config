-- Базовые настройки
--vim.g.did_load_filetypes = 1          -- Отключает загрузку типов файлов плагинами (1 - уже загружено)
vim.g.formatoptions = "qrn1"          -- Настройки автоформатирования (q - форматирование комментариев, r - автоматический ввод комментариев, n - распознавание списков)

-- Настройки интерфейса
vim.opt.showmode = false              -- Скрыть отображение текущего режима (INSERT, VISUAL и т.д.)
vim.opt.updatetime = 100              -- Частота обновления интерфейса (мс) (влияет на CursorHold)
vim.wo.signcolumn = "yes"             -- Всегда показывать колонку для знаков (лайны, ошибки)
vim.opt.wrap = false                  -- Не переносить длинные строки
vim.wo.linebreak = true               -- Переносить строки только в местах переноса слов
vim.opt.virtualedit = "block"         -- Разрешить курсору выходить за текст в визуальном блоке
vim.opt.undofile = true               -- Сохранять историю изменений между перезапусками
vim.opt.shell = "/usr/bin/zsh"        -- Использовать Zsh как оболочку по умолчанию
vim.opt.swapfile = false              -- Отключить создание swap-файлов
vim.opt.encoding = "utf-8"            -- Кодировка файлов по умолчанию
vim.opt.cursorline = true             -- Подсвечивать текущую строку
vim.opt.fileformat = "unix"           -- Формат файлов по умолчанию (Unix-стиль окончаний строк)

-- Настройки Nvim-Tree (файлового менеджера)
vim.g.loaded_netrw = 1                -- Отключить стандартный файловый менеджер Netrw
vim.g.loaded_netrwPlugin = 1          -- Отключить плагин Netrw
vim.opt.termguicolors = true          -- Включить 24-битные цвета в терминалах с поддержкой

-- Настройки прокрутки
vim.opt.so = 30                       -- Вертикальный отступ при скролле (количество строк от края экрана)

-- Настройки поиска
vim.opt.ignorecase = true             -- Игнорировать регистр при поиске
vim.opt.smartcase = true              -- Учитывать регистр при наличии заглавных букв в поиске
vim.opt.hlsearch = true               -- Подсвечивать найденные совпадения
vim.opt.incsearch = true              -- Показывать совпадения во время набора

-- Настройки мыши
vim.opt.mouse = "a"                   -- Включить мышь во всех режимах
vim.opt.mousefocus = true             -- Автоматически фокусировать окно при наведении мыши

-- Номера строк
vim.opt.number = true                 -- Показывать абсолютные номера строк
vim.opt.relativenumber = true         -- Показывать относительные номера строк
vim.wo.number = true                  -- Локальная настройка номеров строк для окна
vim.wo.relativenumber = true          -- Локальная настройка относительных номеров для окна

-- Разделение окон
vim.opt.splitbelow = true             -- Новые окна открывать снизу
vim.opt.splitright = true             -- Новые окна открывать справа

-- Работа с буфером обмена
vim.opt.clipboard = vim.opt.clipboard:append("unnamedplus")    -- Использовать системный буфер обмена (+ регистр)

-- Настройки сообщений
vim.opt.shortmess:append("c")         -- Сокращать сообщения (c - не показывать доп. сообщения при автодополнении)

-- Настройки отступов
vim.opt.expandtab = true              -- Преобразовывать табы в пробелы
vim.opt.shiftwidth = 4                -- Размер отступа при использовании >> и <<
vim.opt.tabstop = 4                   -- Количество пробелов для отображения таба
vim.opt.softtabstop = 4               -- Количество пробелов при вставке таба
vim.opt.smartindent = true            -- Автоматический отступ для новых строк
vim.opt.cindent = true                -- Умные отступы для C-подобных языков
vim.opt.smarttab = true               -- Использовать shiftwidth вместо tabstop в начале строки

-- Символы заполнения
vim.opt.fillchars = {
    vert = "│",                      -- Символ вертикального разделителя
    fold = "⠀",                     -- Символ для свернутых блоков (невидимый)
    eob = " ",                       -- Символ в конце буфера вместо ~
    msgsep = "‾",                    -- Символ разделителя сообщений
    foldopen = "▾",                  -- Символ открытой свертки
    foldsep = "│",                   -- Символ разделителя сверток
    foldclose = "▸"                  -- Символ закрытой свертки
}

-- Очистка стилей номеров строк и колонки знаков
vim.cmd([[highlight clear LineNr]])     -- Сброс стиля номеров строк
vim.cmd([[highlight clear SignColumn]]) -- Сброс стиля колонки знаков
