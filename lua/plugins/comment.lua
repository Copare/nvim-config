require('Comment').setup()

-- Основные горячие клавиши:
-- ~~~~~~~~~~~~~~~~~~~~~~~~~
-- `gcc`       - Переключить комментарий для текущей строки (строчный режим)
-- `gbc`       - Переключить комментарий для текущей строки (блочный режим)
-- `3gcc`      - Переключить комментарии для 3 строк ниже (строчный режим)
-- `5gbc`      - Переключить комментарии для 5 строк ниже (блочный режим)
-- `gcip`      - Переключить комментарий для текста внутри параграфа
-- `gci{`      - Переключить комментарий внутри фигурных скобок

-- Расширенные возможности:
-- ~~~~~~~~~~~~~~~~~~~~~~~~
-- Визуальный режим:
--   Выделите текст -> `gc` - закомментировать выделение (строчный)
--   Выделите текст -> `gb` - закомментировать выделение (блочный)

-- Движения с префиксом:
--   `gcw`  - Закомментировать слово под курсором
--   `gca}` - Закомментировать до конца блока

-- Дополнения:
-- 1. Автоматически определяет тип комментариев для языка (//, #, --, <!-- --> и т.д.)
-- 2. Поддерживает вложенные языки через Treesitter (например, JSX: {/* comments */})
-- 3. Для работы с JSX/Vue установите плагин 'JoosepAlviste/nvim-ts-context-commentstring'

-- Примеры использования:
-- 1. Закомментировать 5 строк: Нажмите `5`, затем `gcc`
-- 2. Закомментировать функцию: Установите курсор внутри функции, нажмите `gcip`
-- 3. Комментировать HTML-атрибут: В JSX выделите атрибут и нажмите `gb`
